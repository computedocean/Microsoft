<#
.SYNOPSIS
    Sign the specified file with a code signing certificate.
    Designed to be used as an explorer send to menu item

.NOTES
    Installation:

    In the user's sendto menu (type shell:sendto in explorer bar), create a shortcut to powershell.exe with arguments -file "THis file.ps1"

    Modification History:

    2023/02/18  @guyrleech  Initial Release
#>


if( $args -and $args.Count )
{
    $signingCertificate = Get-ChildItem -Path Cert:\CurrentUser\My -CodeSigningCert | Where-Object { $_.NotAfter -gt ([datetime]::Now) }
    
    [string]$timeStampServer = $env:TimeStampServer

    if( [string]::IsNullOrEmpty( $timeStampServer ) )
    {
        $timeStampServer = 'http://timestamp.digicert.com'
    }

    if( $null -eq $signingCertificate )
    {
        Add-Type -AssemblyName System.Windows.Forms
        [void][Windows.MessageBox]::Show( 'No valid code signing certificate found' , 'Signing Error', 'Ok' , 'Error' )
        Throw "No valid code signing certificate found"
    }
    if( $signingCertificate -is [array] -and $signingCertificate.Count -gt 1 )
    {
        $chosen = $signingCertificate | Select-Object -Property Thumbprint,Subject,FriendlyName,NotBefore,NotAfter | Out-GridView -Title "Choose 1 of the $($signingCertificate.Count) certificates" -PassThru
        if( -Not $chosen -or ( $chosen -is [array] -and $chosen.Count -ne 1 ))
        {
            Throw "No single certificate selected"
        }
        $signingCertificate = $signingCertificate | Where-Object -Property ThumbPrint -ieq $chosen.Thumbprint
    }

    ForEach( $file in $args )
    {
        $signing = $null
        $signing = Set-AuthenticodeSignature -Certificate $signingCertificate -FilePath $file -TimestampServer $timeStampServer
        if( -not $signing -or -Not $? )
        {
            [void][Windows.MessageBox]::Show( "Error signing $file`n$($Error[0])" , 'Signing Error', 'Ok' , 'Error' )
        }
    }
}

